int		add(int a, int b)
{
	return (a + b);
}

int		diff(int a, int b)
{
	return (a - b);
}

int		multiple(int a, int b)
{
	return ((a * b));
}

int		divide(int a, int b)
{
	return (a / b);
}

int		modular(int a, int b)
{
	return (a % b);
}
